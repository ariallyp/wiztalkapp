package com.yuanxin.app.app.entity.gen;

public class Skin {
	
	 private String id;
	 private String skinName;
	 private String skinType;
	 private String diviceType;
	 private String skinUrl;
	 private int sort;
	 private String skinAvatar;
	 
	 
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSkinName() {
		return skinName;
	}
	public void setSkinName(String skinName) {
		this.skinName = skinName;
	}
	public String getSkinType() {
		return skinType;
	}
	public void setSkinType(String skinType) {
		this.skinType = skinType;
	}
	public String getDiviceType() {
		return diviceType;
	}
	public void setDiviceType(String diviceType) {
		this.diviceType = diviceType;
	}
	public String getSkinUrl() {
		return skinUrl;
	}
	public void setSkinUrl(String skinUrl) {
		this.skinUrl = skinUrl;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public String getSkinAvatar() {
		return skinAvatar;
	}
	public void setSkinAvatar(String skinAvatar) {
		this.skinAvatar = skinAvatar;
	}
	 
	 
	 
	 

}
